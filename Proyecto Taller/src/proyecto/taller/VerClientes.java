    void mostrarTabla() throws SQLException{
        
        DefaultTableModel model= new DefaultTableModel();
        model.addColumn("ID CLIENTE");
        model.addColumn("NOMBRE");
        model.addColumn("APELLIDO PATERNO");
        model.addColumn("APELLIDO MATERNO");
        model.addColumn("TELÉFONO");
        model.addColumn("CALLE");
        model.addColumn("NUM CASA");
        model.addColumn("COLONIA");
        model.addColumn("ELECTRODOMÉSTICO");
        
        tablaClientes.setModel(model);
        
        String sql = "SELECT * FROM cliente";

        String datos[] = new String[9];
        Statement st;
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                datos[7] = rs.getString(8);
                datos[8] = rs.getString(9);
                model.addRow(datos);
            }
            
            tablaClientes.setModel(model);
            

        } catch (SQLException ex) {
            Logger.getLogger(VerClientes.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

