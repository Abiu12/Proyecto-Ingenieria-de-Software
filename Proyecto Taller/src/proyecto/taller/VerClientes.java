    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int fila = tablaClientes.getSelectedRow();
        if (fila >= 0) {
            idCliente = tablaClientes.getValueAt(fila, 0).toString();
            nombre = tablaClientes.getValueAt(fila, 1).toString();

            apPaterno = tablaClientes.getValueAt(fila, 2).toString();

            apMaterno = tablaClientes.getValueAt(fila, 3).toString();

            tel = tablaClientes.getValueAt(fila, 4).toString();

            calle = tablaClientes.getValueAt(fila, 5).toString();

            numCasa = tablaClientes.getValueAt(fila, 6).toString();
            colonia = tablaClientes.getValueAt(fila, 7).toString();
            electrodomestico = tablaClientes.getValueAt(fila, 8).toString();
        }
        else{
            JOptionPane.showMessageDialog(null,"Fila no seleccionada");
        }
        
        ModificarClientes ventanaModificar= new ModificarClientes(idCliente,nombre,apPaterno,apMaterno,tel,calle,numCasa,colonia,electrodomestico);
        ventanaModificar.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //escoger de la tabla e eliminar a los clientes

        int fila= tablaClientes.getSelectedRow(); //fila seleccionada
        String valor= tablaClientes.getValueAt(fila,0).toString(); //regresa el id de la fila seleccionada
        
        if (fila >= 0) {
            try {
                PreparedStatement pps= cn.prepareStatement("DELETE FROM cliente WHERE idCliente='"+valor+"'"); //Eliminamos por el id
                pps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Cliente eliminado!!!");
                mostrarTabla();
                
            } catch (SQLException ex) {
                Logger.getLogger(VerClientes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

